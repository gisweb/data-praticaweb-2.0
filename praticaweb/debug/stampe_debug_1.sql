<p style="text-decoration:underline">Opere consentite.</p>
<p>
Nella "Z.S.A." ogni intervento di trasformazione urbanistica &egrave; subordinato alla
approvazione di uno o pi&ugrave; S.U.A. le cui finalit&agrave; sono il riassetto funzionale
dell'area interessata in termini di spazi pubblici per servizi, parcheggi, viabilit&agrave;
pedonale e veicolare con particolare riferimento a eventuali nuovi tratti stradali non indicati
espressamente nella cartografia, contemplando in particolare la previsione di collegamento stradale,
tra via Roma e Piazzale Rollino, la realizzazione dei connessi parcheggi a raso, la realizzazione
in interrato e seminterrato di parcheggi prevalente a prevalente uso pubblico - che utilizzino
i suddetti parcheggi pubblici in copertura - la sistemazione di Piazza S. Michele con l'ampliamento
previsto lato mare e la realizzazione della sottostante volumetria come incremento dell'attrezzatura
collettiva, il collegamento a monte del Cimitero fra Piazzale Rollino, via Teriasca e via alla
Chiesa.
</p>
<p>
Sono consentite, con concessione edilizia diretta, opere di manutenzione, di consolidamento e di
restauro del patrimonio edilizio esistente, nonch&eacute; ampliamenti, motivati da esigenze di
carattere igienico e funzionale, per una volumetria non superiore al 20 per cento degli edifici
residenziali di volume inferiore a 200 metri cubi, aumentabile del 10 per cento della volumetria
residua per gli edifici di volume compreso tra i 200 e i 500 metri cubi, nel rispetto dei seguenti
parametri:
</p>
<ul>
<li>D c = m. 5</li>
<li>D conf. = m. 2,5</li>
</ul>
<p>
In nessun caso &egrave; consentito l'aumento del numero dei piani riferito al corpo di
fabbrica avente maggiore altezza.
</p>
<p>
Per il calcolo del volume di riferimento si deve considerare quello risultante alla data di adozione
della variante generale al P.R.G..
</p>
<p>
Per attivit&agrave; diverse dalla residenza sono ammessi, con concessione edilizia diretta, oltre
agli interventi di manutenzione e di restauro, interventi di ristrutturazione, motivati da esigenze
di carattere tecnologico o funzionale, con un ampliamento del volume pari al 20 per cento di quello
risultante alla data di adozione della presente normativa. Nello specifico caso in cui tali interventi
di ristrutturazione riguardino il mutamento di destinazione di attrezzature ricettive &egrave; fatto
obbligo di ricorrere alla concessione convenzionata ai fini di garantire una corretta dotazione dell
urbanizzazioni.
</p>
